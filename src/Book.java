public class Book {
  protected static String title;

  protected static int year;

  public Book() {
    this.title = title;
    this.year = year;

  }

  public String getTitle() {
    return this.title;
  }

  public int getYear() {
    return this.year;
  }

  public void setYear() {
    this.year = year;
  }
}
